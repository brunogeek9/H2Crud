package modelo;

/**
 *
 * @author jamelli
 */
public class User {
    private int id, age;
    private String first, last;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getFirst() {
        return first;
    }

    public void setFirst(String first) {
        this.first = first;
    }

    public String getLast() {
        return last;
    }

    public void setLast(String last) {
        this.last = last;
    }

    public User(int age, String first, String last) {
        this.age = age;
        this.first = first;
        this.last = last;
    }
    public User(){}
    
}
